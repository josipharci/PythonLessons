'''
Kreirajte tri brojčane varijable te pomoću njih isprobajte funkcije za 
rad s brojevima (int(), float(), complex(), hex(), oct()).

'''

a = 15
b = 10.5
c = 5.23

print("int a:",int(a),"int b:",int(b),"int c:",int(c))
print("float a:",float(a),"float b:",float(b),"float c:",float(c))
print("complex a:",complex(a),"complex b:",complex(b),"complex c:",complex(c))
print("hex a:",hex(a))
print("oct a:",oct(a))

y = int(b)
print("hex b:",hex(y))
print("oct b:",oct(y))

x = int(c)
print("hex c:",hex(x))
print("oct c:",oct(x))
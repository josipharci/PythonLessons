'''
Kreirajte dvije brojčane varijable te ispišite rezultat operatora 
usporedbe (usporedba jednakosti, nejednakosti, strogo veće, strogo 
manje, veće ili jednako, manje ili jednako)

'''

num1 = 10
num2 = 15

# jednakost 
print(num1 == num2)

# nejdednakost 
print(num1 != num2)

# strogo veče 
print(num1 > num2)

# strogo manje 
print(num1 < num2)

# veće ili jednako
print(num1 >= num2)

# manje ili jednako
print(num1 <= num2)

'''
Kreirajte dvije varijable i pridijelite im vrijednosti True ili False. 
Ispišite rezultate logičkih operatora (and, or, not) za dvije 
prethodno kreirane varijable

'''

var1 = False
var2 = True

# And
print(var1 and var2 , "AND")

# Or
print(var1 or var2 , "OR")

# Not
print(not var1 , "NOT") 
print(not var2 , "NOT")
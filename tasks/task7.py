'''Kreirajte dvije brojčane varijable te ispišite rezultat operatora 
usporedbe (usporedba jednakosti, nejednakosti, strogo veće, strogo 
manje, veće ili jednako, manje ili jednako)'''

num1 = 10
num2 = 15

# jednakost 
print(num1 == num2)

# nejdednakost 
print(num1 != num2)

# strogo veče 
print(num1 > num2)

# strogo manje 
print(num1 < num2)

# veće ili jednako
print(num1 >= num2)

# manje ili jednako
print(num1 <= num2)